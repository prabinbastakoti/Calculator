

// Add two items
const add = function(a , b) {
    return a + b;
}

// Subtract two items
const subtract = function(a , b) {
    return a - b;
}

// Multiply two items
const multiply = function(a , b) {
    return a * b;
}

// Divide two items
const divide = function(a , b) {
    return a / b;
}

